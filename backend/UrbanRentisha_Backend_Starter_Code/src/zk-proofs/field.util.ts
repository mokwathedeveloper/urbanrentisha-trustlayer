const MIMC_ROUNDS = 220;

/**
 * One-way compression permutation over the BLS12-381 scalar field, using the
 * x^5 S-box with a fixed `right` value injected every round (Davies-Meyer
 * style - not a Feistel network, there is no left/right swap). Must produce
 * bit-for-bit identical output to the circuit's MiMCPermutation template for
 * the same inputs, since this is what binds the proof's private witness to
 * the on-chain paymentCommitment.
 */
export function mimcPermutation(left: bigint, right: bigint): bigint {
  let state = left % BLS12_381_SCALAR_FIELD;
  for (let i = 0; i < MIMC_ROUNDS; i++) {
    const added = addMod(state, right, MIMC_ROUND_CONSTANTS[i % 220]);
    state = powMod(added, 5n, BLS12_381_SCALAR_FIELD);
  }
  return addMod(state, right);
}
